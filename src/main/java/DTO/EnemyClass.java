package DTO;

public class EnemyClass
{
    private int id;
    private String e_name;
    private int e_hp;

    public int getId()
    {
        return id;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public String getE_name()
    {
        return e_name;
    }

    public void setE_name(String e_name)
    {
        this.e_name = e_name;
    }

    public int getE_hp()
    {
        return e_hp;
    }

    public void setE_hp(int e_hp)
    {
        this.e_hp = e_hp;
    }


}
