package DTO;

public class ItemClass
{
    private int id;
    private String name;
    private String att;
    private int dem;
    private String hyo;

    public int getId()
    {
        return id;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getAtt()
    {
        return att;
    }

    public void setAtt(String att)
    {
        this.att = att;
    }

    public int getDem()
    {
        return dem;
    }

    public void setDem(int dem)
    {
        this.dem = dem;
    }

    public String getHyo()
    {
        return hyo;
    }

    public void setHyo(String hyo)
    {
        this.hyo = hyo;
    }
}
